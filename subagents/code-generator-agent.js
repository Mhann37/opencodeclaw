#!/usr/bin/env node

/**
 * AUTONOMOUS CODE GENERATOR SUBAGENT
 * 
 * Spawned by Jarvis to autonomously generate code using OpenCode + Ollama
 * No subscription LLMs, fully local execution
 * 
 * Usage: node code-generator-agent.js <task-json>
 * Task JSON: { "id": "X", "description": "Build Y", "language": "javascript" }
 */

const fs = require('fs');
const path = require('path');
const { exec } = require('child_process');
const { promisify } = require('util');

const execAsync = promisify(exec);

class CodeGeneratorAgent {
  constructor(task) {
    this.task = task;
    this.id = task.id || `task_${Date.now()}`;
    this.language = task.language || 'javascript';
    this.outputDir = path.join('/var/lib/openclaw/.openclaw/workspace', 'generated', this.id);
    this.logFile = path.join(this.outputDir, 'generation.log');
  }

  async initialize() {
    // Create output directory
    if (!fs.existsSync(this.outputDir)) {
      fs.mkdirSync(this.outputDir, { recursive: true });
    }
    
    this.log(`[INIT] Task: ${this.id}`);
    this.log(`[INIT] Description: ${this.task.description}`);
    this.log(`[INIT] Language: ${this.language}`);
    this.log(`[INIT] Output: ${this.outputDir}`);
  }

  log(message) {
    const timestamp = new Date().toISOString();
    const logMessage = `[${timestamp}] ${message}\n`;
    console.log(logMessage.trim());
    fs.appendFileSync(this.logFile, logMessage);
  }

  /**
   * Invoke Ollama via curl (no opencode dependency, direct API)
   */
  async invokeOllama() {
    this.log(`[OLLAMA] Invoking orca-mini...`);
    
    try {
      const prompt = this.buildPrompt();
      
      // Call Ollama API directly
      const cmd = `curl -s -X POST http://localhost:11434/api/generate \\
        -d '{
          "model": "orca-mini",
          "prompt": "${prompt.replace(/"/g, '\\"')}",
          "stream": false
        }'`;
      
      this.log(`[OLLAMA] Prompt: ${prompt.substring(0, 100)}...`);
      
      const { stdout } = await execAsync(cmd, { timeout: 60000 });
      const response = JSON.parse(stdout);
      
      if (!response.response) {
        throw new Error('No response from Ollama');
      }
      
      this.log(`[OLLAMA] ✓ Generated ${response.response.length} characters`);
      
      return response.response;
    } catch (e) {
      this.log(`[OLLAMA] ✗ Error: ${e.message}`);
      throw e;
    }
  }

  buildPrompt() {
    return `You are an expert software engineer. Write clean, production-ready code.

Task: ${this.task.description}

Language: ${this.language}

Requirements:
- Include error handling
- Add comments for clarity
- Follow ${this.language} best practices
- Make it ready for production

Generate ONLY the code, no explanations.`;
  }

  /**
   * Save generated code to file
   */
  async saveCode(code) {
    const ext = this.language === 'javascript' ? 'js' : 'py';
    const filepath = path.join(this.outputDir, `generated.${ext}`);
    
    fs.writeFileSync(filepath, code);
    this.log(`[SAVE] ✓ Saved to ${filepath}`);
    
    return filepath;
  }

  /**
   * Validate code (basic ESLint for JS)
   */
  async validateCode(filepath) {
    this.log(`[VALIDATE] Running validation...`);
    
    if (this.language !== 'javascript') {
      this.log(`[VALIDATE] Skipping validation (non-JS)`);
      return { valid: true, errors: [] };
    }
    
    try {
      const { stdout, stderr } = await execAsync(
        `node -c "${filepath}" 2>&1 || true`,
        { timeout: 5000 }
      );
      
      if (stderr || stdout.includes('SyntaxError')) {
        this.log(`[VALIDATE] ⚠ Syntax issues detected`);
        return { valid: false, errors: [stderr || stdout] };
      }
      
      this.log(`[VALIDATE] ✓ Code passes basic syntax check`);
      return { valid: true, errors: [] };
    } catch (e) {
      this.log(`[VALIDATE] ✗ Validation error: ${e.message}`);
      return { valid: false, errors: [e.message] };
    }
  }

  /**
   * Generate README for the code
   */
  async generateDocs(code) {
    this.log(`[DOCS] Generating README...`);
    
    const readme = `# Generated Code: ${this.id}

## Description
${this.task.description}

## Generated At
${new Date().toISOString()}

## Language
${this.language}

## Usage
\`\`\`bash
# For JavaScript:
node generated.js

# For Python:
python3 generated.py
\`\`\`

## Generated Code
\`\`\`${this.language}
${code}
\`\`\`

## Notes
- Auto-generated by Autonomous Coding Team
- Review before production use
- All code follows best practices
`;
    
    const readmePath = path.join(this.outputDir, 'README.md');
    fs.writeFileSync(readmePath, readme);
    
    this.log(`[DOCS] ✓ README created`);
    return readmePath;
  }

  /**
   * Main execution
   */
  async execute() {
    try {
      await this.initialize();
      
      // Step 1: Generate code
      const code = await this.invokeOllama();
      
      // Step 2: Save code
      const filepath = await this.saveCode(code);
      
      // Step 3: Validate code
      const validation = await this.validateCode(filepath);
      
      // Step 4: Generate docs
      const docsPath = await this.generateDocs(code);
      
      // Final result
      const result = {
        status: validation.valid ? 'success' : 'warning',
        id: this.id,
        filepath,
        docsPath,
        validation,
        logPath: this.logFile,
        timestamp: new Date().toISOString()
      };
      
      this.log(`[COMPLETE] ✓ Task completed with status: ${result.status}`);
      
      // Output JSON for parent process
      console.log('\n[RESULT]');
      console.log(JSON.stringify(result, null, 2));
      
      return result;
    } catch (e) {
      this.log(`[ERROR] ✗ Execution failed: ${e.message}`);
      
      const result = {
        status: 'error',
        id: this.id,
        error: e.message,
        logPath: this.logFile,
        timestamp: new Date().toISOString()
      };
      
      console.log('\n[RESULT]');
      console.log(JSON.stringify(result, null, 2));
      
      process.exit(1);
    }
  }
}

// CLI Usage
if (require.main === module) {
  const taskJson = process.argv[2];
  
  if (!taskJson) {
    console.error('Usage: node code-generator-agent.js <task-json>');
    console.error('Example: node code-generator-agent.js \'{"id":"task1","description":"Build a hello world script","language":"javascript"}\'');
    process.exit(1);
  }
  
  try {
    const task = JSON.parse(taskJson);
    const agent = new CodeGeneratorAgent(task);
    agent.execute();
  } catch (e) {
    console.error(`Failed to parse task: ${e.message}`);
    process.exit(1);
  }
}

module.exports = CodeGeneratorAgent;
